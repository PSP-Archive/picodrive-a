/*
 * Comparison function for qsort()
 * For entries with an equal number of set bits in
 * the mask compare the match values
 */
static int /* DECL_SPEC */ compare_nof_true_bits(
const void* aptr, const void* bptr)
{
	const opcode_handler_struct *a = aptr, *b = bptr;
	if( a->bits != b->bits )
		return a->bits - b->bits;
	if( a->mask != b->mask )
		return a->mask - b->mask;
	return a->match - b->match;
}


#define CUTOFF 8			/* testing shows that this is good value */

static void swap (
	char *a,
	char *b,
	unsigned width
	)
{
	char tmp;

	if ( a != b )
		/* Do the swap one character at a time to a void potential alignment
		   problems. */
		while ( width-- ) {
			tmp = *a;
			*a++ = *b;
			*b++ = tmp;
		}
}
static void shortsort (
	char *lo,
	char *hi,
	unsigned width//,
//	int (*comp)(const void *, const void *)
	)
{
	char *p, *max;

	/* Note: in assertions below, i and j are alway inside original bound of
	   array to sort. */

	while (hi > lo) {
		/* A[i] <= A[j] for i <= j, j > hi */
		max = lo;
		for (p = lo+width; p <= hi; p += width) {
			/* A[i] <= A[max] for lo <= i < p */
			if (compare_nof_true_bits(p, max) > 0) {
				max = p;
			}
			/* A[i] <= A[max] for lo <= i <= p */
		}

		/* A[i] <= A[max] for lo <= i <= hi */

		swap(max, hi, width);

		/* A[i] <= A[hi] for i <= hi, so A[i] <= A[j] for i <= j, j >= hi */

		hi -= width;

		/* A[i] <= A[j] for i <= j, j > hi, loop top condition established */
	}
	/* A[i] <= A[j] for i <= j, j > lo, which implies A[i] <= A[j] for i < j,
	   so array is sorted */
}
static void my_qsort (
	void *base,
	unsigned num,
	unsigned width//,
	//int (*comp)(const void *, const void *)
	)
{
	char *lo, *hi;				/* ends of sub-array currently sorting */
	char *mid;					/* points to middle of subarray */
	char *loguy, *higuy;		/* traveling pointers for partition step */
	unsigned size;				/* size of the sub-array */
	char *lostk[30], *histk[30];
	int stkptr; 				/* stack for saving sub-array to be processed */

	/* Note: the number of stack entries required is no more than
	   1 + log2(size), so 30 is sufficient for any array */

	if (num < 2 || width == 0)
		return; 				/* nothing to do */

	stkptr = 0; 				/* initialize stack */

	lo = base;
	hi = (char *)base + width * (num-1);		/* initialize limits */

	/* this entry point is for pseudo-recursion calling: setting
	   lo and hi and jumping to here is like recursion, but stkptr is
	   prserved, locals aren't, so we preserve stuff on the stack */
recurse:

	size = (hi - lo) / width + 1;		 /* number of el's to sort */

	/* below a certain size, it is faster to use a O(n^2) sorting method */
	if (size <= CUTOFF) {
		 shortsort(lo, hi, width/*, comp*/);
	}
	else {
		/* First we pick a partititioning element.	The efficiency of the
		   algorithm demands that we find one that is approximately the
		   median of the values, but also that we select one fast.	Using
		   the first one produces bad performace if the array is already
		   sorted, so we use the middle one, which would require a very
		   wierdly arranged array for worst case performance.  Testing shows
		   that a median-of-three algorithm does not, in general, increase
		   performance. */

		mid = lo + (size / 2) * width;		/* find middle element */
		swap(mid, lo, width);				/* swap it to beginning of array */

		/* We now wish to partition the array into three pieces, one
		   consisiting of elements <= partition element, one of elements
		   equal to the parition element, and one of element >= to it.	This
		   is done below; comments indicate conditions established at every
		   step. */

		loguy = lo;
		higuy = hi + width;

		/* Note that higuy decreases and loguy increases on every iteration,
		   so loop must terminate. */
		for (;;) {
			/* lo <= loguy < hi, lo < higuy <= hi + 1,
			   A[i] <= A[lo] for lo <= i <= loguy,
			   A[i] >= A[lo] for higuy <= i <= hi */

			do	{
				loguy += width;
			} while (loguy <= hi && compare_nof_true_bits(loguy, lo) <= 0);

			/* lo < loguy <= hi+1, A[i] <= A[lo] for lo <= i < loguy,
			   either loguy > hi or A[loguy] > A[lo] */

			do	{
				higuy -= width;
			} while (higuy > lo && compare_nof_true_bits(higuy, lo) >= 0);

			/* lo-1 <= higuy <= hi, A[i] >= A[lo] for higuy < i <= hi,
			   either higuy <= lo or A[higuy] < A[lo] */

			if (higuy < loguy)
				break;

			/* if loguy > hi or higuy <= lo, then we would have exited, so
			   A[loguy] > A[lo], A[higuy] < A[lo],
			   loguy < hi, highy > lo */

			swap(loguy, higuy, width);

			/* A[loguy] < A[lo], A[higuy] > A[lo]; so condition at top
			   of loop is re-established */
		}

		/*	   A[i] >= A[lo] for higuy < i <= hi,
			   A[i] <= A[lo] for lo <= i < loguy,
			   higuy < loguy, lo <= higuy <= hi
		   implying:
			   A[i] >= A[lo] for loguy <= i <= hi,
			   A[i] <= A[lo] for lo <= i <= higuy,
			   A[i] = A[lo] for higuy < i < loguy */

		swap(lo, higuy, width); 	/* put partition element in place */

		/* OK, now we have the following:
			  A[i] >= A[higuy] for loguy <= i <= hi,
			  A[i] <= A[higuy] for lo <= i < higuy
			  A[i] = A[lo] for higuy <= i < loguy	 */

		/* We've finished the partition, now we want to sort the subarrays
		   [lo, higuy-1] and [loguy, hi].
		   We do the smaller one first to minimize stack usage.
		   We only sort arrays of length 2 or more.*/

		if ( higuy - 1 - lo >= hi - loguy ) {
			if (lo + width < higuy) {
				lostk[stkptr] = lo;
				histk[stkptr] = higuy - width;
				++stkptr;
			}							/* save big recursion for later */

			if (loguy < hi) {
				lo = loguy;
				goto recurse;			/* do small recursion */
			}
		}
		else {
			if (loguy < hi) {
				lostk[stkptr] = loguy;
				histk[stkptr] = hi;
				++stkptr;				/* save big recursion for later */
			}

			if (lo + width < higuy) {
				hi = higuy - width;
				goto recurse;			/* do small recursion */
			}
		}
	}

	/* We have sorted the array, except for any pending sorts on the stack.
	   Check if there are any, and do them. */

	--stkptr;
	if (stkptr >= 0) {
		lo = lostk[stkptr];
		hi = histk[stkptr];
		goto recurse;			/* pop subarray from stack */
	}
	else
		return; 				/* all subarrays done */
}






/* Build the opcode handler jump table */
static void/*test*/ m68ki_build_opcode_table(void)
{
	opcode_handler_struct *ostruct;
	int instr;
	int i;
	int j;
	uint table_length = 0;

	for(ostruct = m68k_opcode_handler_table;ostruct->opcode_handler != 0;ostruct++)
		table_length++;
	my_qsort(( void *)m68k_opcode_handler_table, table_length, sizeof(m68k_opcode_handler_table[0])/*, compare_nof_true_bits*/);

	for(i = 0; i < 0x10000; i++)
	{
		/* default to illegal */
		m68ki_instruction_jump_table[i] = m68k_op_illegal;
	}

	ostruct = m68k_opcode_handler_table;
	while(ostruct->mask != 0xff00)
	{
		for(i = 0;i < 0x10000;i++)
		{
			if((i & ostruct->mask) == ostruct->match)
			{
				m68ki_instruction_jump_table[i] = ostruct->opcode_handler;
			}
		}
		ostruct++;
	}
	/* bcc */
	while(ostruct->mask == 0xff00)
	{
		for(i = 0;i <= 0xff;i++)
		{
			m68ki_instruction_jump_table[ostruct->match | i] = ostruct->opcode_handler;
		}
		ostruct++;
	}
	while(ostruct->mask == 0xf1f8)
	{
		for(i = 0;i < 8;i++)
		{
			for(j = 0;j < 8;j++)
			{
				instr = ostruct->match | (i << 9) | j;
				m68ki_instruction_jump_table[instr] = ostruct->opcode_handler;
			}
		}
		ostruct++;
	}
	while(ostruct->mask == 0xfff0)
	{
		for(i = 0;i <= 0x0f;i++)
		{
			m68ki_instruction_jump_table[ostruct->match | i] = ostruct->opcode_handler;
		}
		ostruct++;
	}
	while(ostruct->mask == 0xf1ff)
	{
		for(i = 0;i <= 0x07;i++)
		{
			m68ki_instruction_jump_table[ostruct->match | (i << 9)] = ostruct->opcode_handler;
		}
		ostruct++;
	}
	while(ostruct->mask == 0xfff8)
	{
		for(i = 0;i <= 0x07;i++)
		{
			m68ki_instruction_jump_table[ostruct->match | i] = ostruct->opcode_handler;
		}
		ostruct++;
	}
	while(ostruct->mask == 0xffff)
	{
		m68ki_instruction_jump_table[ostruct->match] = ostruct->opcode_handler;
		ostruct++;
	}
}
